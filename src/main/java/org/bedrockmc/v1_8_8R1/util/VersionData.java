package org.bedrockmc.v1_8_8R1.util;

public class VersionData {

	private String MinecraftVersion;
	private String BedrockVersion;
	private int VersionCode;
	private String DirectDownloadUrl;
	private String InstallerDownloadUrl;

	public String getMinecraftVersion() {
		return MinecraftVersion;
	}

	public String getBedrockVersion() {
		return BedrockVersion;
	}

	public int getVersionCode() {
		return VersionCode;
	}

	public String getDirectDownloadUrl() {
		return DirectDownloadUrl;
	}

	public String getInstallerDownloadUrl() {
		return InstallerDownloadUrl;
	}

}
